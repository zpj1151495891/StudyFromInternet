package com.springWeb.Model;

public class Student {
	private Integer id;
	private String name;
	private Integer age;
	
	public void setId(Integer id) {
		this.id=id;
	}
	public Integer getId() {
		return id;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	
	public void setAge(Integer age) {
		this.age=age;
	}
	public Integer getAge() {
		return age;
	}

}
